/// embed.rs — CPU embedding inference via `tract-onnx` (pure Rust, no C deps) + HuggingFace
/// `tokenizers`.
///
/// The public interface is [`Embedder`], which exposes:
///
/// - [`Embedder::load`]        — load a real ONNX model from disk
/// - [`Embedder::stub`]        — deterministic random-unit-vector embedder for testing
/// - [`Embedder::embed_batch`] — embed a slice of strings, returns Vec<Vec<f32>>
/// - [`Embedder::embed_one`]   — convenience wrapper for a single string
/// - [`Embedder::model_sha256`] — sha256 hex of the model file bytes (for DB meta check)
///
/// ## Real model
///
/// [`Embedder::load`] accepts a path to an `.onnx` file.  The tokenizer is expected at
/// `{model_dir}/tokenizer.json`.
///
/// The model takes standard BERT-style inputs and outputs either `sentence_embedding`
/// (pre-pooled) or `last_hidden_state` (mean-pooled by us).  All vectors are L2-normalised.
///
/// ## Stub mode
///
/// [`Embedder::stub`] returns deterministic unit vectors generated by hashing each
/// input text through a lightweight LCG seeded from the text's FNV-1a hash.
use std::path::Path;

use anyhow::{Context, Result};
use sha2::{Digest, Sha256};
use tract_onnx::prelude::*;

// ---------------------------------------------------------------------------
// Type alias for the compiled runnable model.
//
// The chain is:
//   tract_onnx::onnx().model_for_path(...)  → InferenceModel
//   .into_optimized()?                      → TypedModel
//   .into_runnable()?                       → TypedSimplePlan<TypedModel>
//
// TypedModel   = Graph<TypedFact, Box<dyn TypedOp>>
// TypedSimplePlan<M> = SimplePlan<TypedFact, Box<dyn TypedOp>, M>
// ---------------------------------------------------------------------------
type TractModel = TypedSimplePlan<TypedModel>;

// ---------------------------------------------------------------------------
// Public types
// ---------------------------------------------------------------------------

/// The embedding engine.
pub struct Embedder {
    inner: Box<EmbedderInner>,
}

// ---------------------------------------------------------------------------
// Private inner representation
// ---------------------------------------------------------------------------

enum EmbedderInner {
    /// Real tract-onnx ONNX model.
    Tract {
        model: Box<TractModel>,
        tokenizer: Box<tokenizers::Tokenizer>,
        max_tokens: usize,
        dim: usize,
        /// Whether the model accepts `token_type_ids` (probed during load, then cached).
        has_token_type_ids: bool,
        /// Whether the first output is named `sentence_embedding` (pre-pooled).
        /// If false we mean-pool `last_hidden_state` ourselves.
        is_pre_pooled: bool,
    },

    /// HTTP embedding backend (Ollama / OpenAI-compatible).
    Http {
        url: String,
        model: String,
        dim: usize,
    },

    /// Stub: deterministic random unit vectors derived from text hashes.
    Stub { dim: usize },
}

// ---------------------------------------------------------------------------
// impl Embedder
// ---------------------------------------------------------------------------

impl Embedder {
    /// Load an embedding model from `model_path`.
    ///
    /// `model_path` must point to the `.onnx` file.  The tokenizer is expected
    /// at `{model_path_dir}/tokenizer.json`.
    pub fn load(model_path: &Path, max_tokens: usize) -> Result<Self> {
        let model_dir = model_path
            .parent()
            .with_context(|| format!("model_path has no parent: {}", model_path.display()))?;

        // --- Tokenizer ---
        let tokenizer_path = model_dir.join("tokenizer.json");
        let mut tokenizer = tokenizers::Tokenizer::from_file(&tokenizer_path).map_err(|e| {
            anyhow::anyhow!(
                "failed to load tokenizer from {}: {}",
                tokenizer_path.display(),
                e
            )
        })?;

        // Configure truncation so all sequences are capped at max_tokens.
        tokenizer
            .with_truncation(Some(tokenizers::TruncationParams {
                max_length: max_tokens,
                strategy: tokenizers::TruncationStrategy::LongestFirst,
                stride: 0,
                direction: tokenizers::TruncationDirection::Right,
            }))
            .map_err(|e| anyhow::anyhow!("failed to set truncation: {}", e))?;

        // --- Load the ONNX graph (InferenceModel → optimized TypedModel → runnable plan) ---
        //
        // Some models (e.g. gte-multilingual-base) embed symbolic shape expressions like
        // `Min(65536, sequence_length)` in their input dim_params.  tract's TDim parser can
        // only handle simple names and arithmetic — not function calls — so loading fails.
        //
        // Fix: read the raw ONNX bytes, decode the protobuf, clear all dim_params from inputs
        // (replacing with concrete dim_value = max_tokens for seq dim, 1 for batch), re-encode,
        // and feed to tract via model_for_read.  This is safe: we supply our own input facts
        // anyway, so the proto annotations are informational only.
        use prost::Message as _;
        use tract_onnx::prelude::*;

        let raw = std::fs::read(model_path)
            .with_context(|| format!("reading ONNX file {}", model_path.display()))?;

        let mut proto = tract_onnx::pb::ModelProto::decode(raw.as_slice())
            .with_context(|| format!("decoding ONNX protobuf from {}", model_path.display()))?;

        // Clear symbolic dim_params on every graph input; supply concrete values instead.
        // ONNX shape: dim_value = 0 means dynamic; we set batch=1, seq=max_tokens.
        //
        // Also clear graph.value_info entirely: intermediate tensors may contain
        // symbolic expressions like `Min(65536, sequence_length)` that tract's TDim
        // parser cannot handle (it only supports simple names and arithmetic).
        // Removing value_info is safe — tract infers all intermediate shapes from
        // the input facts we supply below.
        if let Some(graph) = proto.graph.as_mut() {
            for input in graph.input.iter_mut() {
                if let Some(type_proto) = input.r#type.as_mut() {
                    if let Some(tract_onnx::pb::type_proto::Value::TensorType(ref mut tt)) =
                        type_proto.value
                    {
                        if let Some(shape) = tt.shape.as_mut() {
                            for (i, dim) in shape.dim.iter_mut().enumerate() {
                                use tract_onnx::pb::tensor_shape_proto::dimension::Value;
                                // Replace any symbolic expression tract can't parse with concrete.
                                if matches!(dim.value, Some(Value::DimParam(_))) {
                                    dim.value = Some(Value::DimValue(if i == 0 {
                                        1
                                    } else {
                                        max_tokens as i64
                                    }));
                                }
                            }
                        }
                    }
                }
            }

            // Strip intermediate shape annotations that may contain unparseable
            // symbolic expressions (e.g. `Min(65536, sequence_length)` in rotary
            // embedding nodes).  tract derives these shapes from input facts.
            graph.value_info.clear();
        }

        let patched = proto.encode_to_vec();
        let mut inference_model = tract_onnx::onnx()
            .model_for_read(&mut patched.as_slice())
            .with_context(|| format!("loading ONNX model from {}", model_path.display()))?;

        // Detect how many inputs the model expects so we know whether it accepts token_type_ids.
        let input_count = inference_model
            .input_outlets()
            .context("reading model input outlets")?
            .len();
        let has_token_type_ids = input_count >= 3;

        // Supply input facts: dynamic batch (any size), fixed seq_len = max_tokens.
        // Using a symbolic batch dimension lets us embed N texts in one forward pass
        // while still giving tract enough shape information to optimize the graph.
        let fact = InferenceFact::dt_shape(i64::datum_type(), tvec!(1usize, max_tokens));
        for i in 0..input_count {
            inference_model
                .set_input_fact(i, fact.clone())
                .with_context(|| format!("setting input fact for input {i}"))?;
        }

        let typed_model = inference_model
            .into_optimized()
            .context("optimizing ONNX model")?;

        // Detect output name before compiling to a plan.
        // The runnable plan exposes the same outlet_labels as the typed model.
        // We look for a `sentence_embedding` output outlet label.
        let output_outlets = typed_model
            .output_outlets()
            .context("reading model output outlets")?
            .to_vec(); // clone so we don't hold a borrow
        let is_pre_pooled = output_outlets
            .iter()
            .any(|&o| typed_model.outlet_label(o) == Some("sentence_embedding"));

        let model = typed_model
            .into_runnable()
            .context("compiling ONNX model to runnable plan")?;

        // Probe the actual output dimension with a single forward pass (batch=1, seq=max_tokens).
        let dim = probe_dim(
            &model,
            has_token_type_ids,
            is_pre_pooled,
            max_tokens,
            &tokenizer,
        )
        .context("probing embedding dimension")?;

        Ok(Self {
            inner: Box::new(EmbedderInner::Tract {
                model: Box::new(model),
                tokenizer: Box::new(tokenizer),
                max_tokens,
                dim,
                has_token_type_ids,
                is_pre_pooled,
            }),
        })
    }

    /// Create a stub embedder that returns deterministic random unit vectors.
    pub fn stub(dim: usize) -> Self {
        Self {
            inner: Box::new(EmbedderInner::Stub { dim }),
        }
    }

    /// Create an HTTP embedder that calls an Ollama-compatible API.
    ///
    /// Probes the endpoint once to determine the embedding dimension.
    pub fn http(url: &str, model: &str) -> Result<Self> {
        // Probe dimension with a test request.
        let probe = http_embed_request(url, model, &["hello"])?;
        let dim = probe.first()
            .ok_or_else(|| anyhow::anyhow!("HTTP embed probe returned empty result"))?
            .len();

        Ok(Self {
            inner: Box::new(EmbedderInner::Http {
                url: url.to_string(),
                model: model.to_string(),
                dim,
            }),
        })
    }

    /// Return the model name for metadata (used by store to detect model changes).
    pub fn model_name(&self) -> &str {
        match self.inner.as_ref() {
            EmbedderInner::Tract { .. } => "tract-onnx",
            EmbedderInner::Http { model, .. } => model,
            EmbedderInner::Stub { .. } => "stub",
        }
    }

    /// Embed a batch of texts and return one unit-normalised vector per text.
    ///
    /// The returned `Vec` has the same length as `texts`; each inner `Vec<f32>`
    /// has length equal to the model's embedding dimension.
    pub fn embed_batch(&self, texts: &[&str]) -> Result<Vec<Vec<f32>>> {
        embed_batch_inner(&self.inner, texts)
    }

    /// Convenience wrapper: embed a single text.
    pub fn embed_one(&self, text: &str) -> Result<Vec<f32>> {
        let mut batch = self.embed_batch(&[text])?;
        batch
            .pop()
            .ok_or_else(|| anyhow::anyhow!("embed_batch returned empty result"))
    }

    /// Return the lowercase hex sha256 of the model file at `model_path`.
    ///
    /// Stored in `meta.model_sha256` so vec can detect when the model file
    /// has been replaced and prompt the user to run `vec updatedb --full`.
    #[allow(dead_code)]
    pub fn model_sha256(model_path: &Path) -> Result<String> {
        let bytes = std::fs::read(model_path)
            .with_context(|| format!("reading model file: {}", model_path.display()))?;
        let digest = Sha256::digest(&bytes);
        Ok(hex::encode(digest))
    }
}

// ---------------------------------------------------------------------------
// Dimension probe: run a tiny forward pass to learn the output dimension.
// ---------------------------------------------------------------------------

/// Run a minimal forward pass to discover the embedding dimension.
///
/// `seq` must match the sequence length the model was compiled for — the model
/// plan is fixed-shape, so feeding a different length causes a shape clash.
fn probe_dim(
    model: &TractModel,
    has_token_type_ids: bool,
    is_pre_pooled: bool,
    seq: usize,
    tokenizer: &tokenizers::Tokenizer,
) -> Result<usize> {
    let batch = 1usize;
    // Tokenize a real short string so the probe uses IDs from the model's
    // actual vocabulary — not hardcoded BERT CLS/SEP IDs (101/102), which are
    // wrong for non-BERT-family models such as XLMRoberta-based
    // gte-multilingual-base.
    let enc = tokenizer
        .encode("hello", true)
        .map_err(|e| anyhow::anyhow!("probe tokenization failed: {}", e))?;
    let raw_ids = enc.get_ids();
    let ids: Vec<i64> = (0..seq)
        .map(|i| raw_ids.get(i).copied().unwrap_or(0) as i64)
        .collect();
    let mask: Vec<i64> = (0..seq)
        .map(|i| if i < raw_ids.len() { 1 } else { 0 })
        .collect();
    let types: Vec<i64> = vec![0; seq];

    let outputs = run_tract(model, has_token_type_ids, batch, seq, &ids, &mask, &types)?;

    // TValue implements Deref<Target=Tensor>; &* dereferences through that impl.
    let out0: &Tensor = &outputs[0];
    let shape = out0.shape();
    // sentence_embedding: [batch, dim]
    // last_hidden_state:   [batch, seq_len, dim]
    let dim = if is_pre_pooled {
        *shape
            .get(1)
            .ok_or_else(|| anyhow::anyhow!("unexpected sentence_embedding shape: {shape:?}"))?
    } else {
        *shape
            .get(2)
            .ok_or_else(|| anyhow::anyhow!("unexpected last_hidden_state shape: {shape:?}"))?
    };
    Ok(dim)
}

// ---------------------------------------------------------------------------
// Core inference (standalone to avoid self-borrow conflicts)
// ---------------------------------------------------------------------------

fn embed_batch_inner(inner: &EmbedderInner, texts: &[&str]) -> Result<Vec<Vec<f32>>> {
    match inner {
        EmbedderInner::Stub { dim } => {
            let d = *dim;
            Ok(texts.iter().map(|t| stub_embed(t, d)).collect())
        }
        EmbedderInner::Tract {
            model,
            tokenizer,
            dim,
            max_tokens,
            has_token_type_ids,
            is_pre_pooled,
        } => {
            if texts.is_empty() {
                return Ok(vec![]);
            }

            let seq = *max_tokens;
            let d = *dim;
            let htti = *has_token_type_ids;
            let pre_pooled = *is_pre_pooled;

            // Embed texts in parallel — TractModel::run(&self) and
            // Tokenizer::encode(&self) are both Send + Sync.
            use rayon::prelude::*;

            let results: Vec<Result<Vec<f32>>> = texts
                .par_iter()
                .map(|&text| {
                    let enc = tokenizer
                        .encode(text, true)
                        .map_err(|e| anyhow::anyhow!("tokenization failed: {}", e))?;

                    let raw_ids = enc.get_ids();
                    if raw_ids.is_empty() {
                        return Ok(vec![0f32; d]);
                    }

                    let ids: Vec<i64> = (0..seq)
                        .map(|i| raw_ids.get(i).copied().unwrap_or(0) as i64)
                        .collect();
                    let mask: Vec<i64> = (0..seq)
                        .map(|i| if i < raw_ids.len() { 1 } else { 0 })
                        .collect();
                    let types: Vec<i64> = vec![0i64; seq];

                    let outputs = run_tract(model, htti, 1, seq, &ids, &mask, &types)?;
                    let emb = extract_embeddings(&outputs, d, pre_pooled, &mask, 1, seq)?;
                    Ok(emb.into_iter().next().unwrap_or_else(|| vec![0f32; d]))
                })
                .collect();

            // Propagate the first error, if any.
            results.into_iter().collect()
        }
        EmbedderInner::Http { url, model, dim } => {
            if texts.is_empty() {
                return Ok(vec![]);
            }
            // Send in small batches to avoid overwhelming the HTTP endpoint.
            // The caller may pass hundreds of texts (scaled by thread count).
            const HTTP_BATCH: usize = 32;
            let mut embeddings = Vec::with_capacity(texts.len());
            for chunk in texts.chunks(HTTP_BATCH) {
                let mut batch = http_embed_request(url, model, chunk)?;
                embeddings.append(&mut batch);
            }
            // Normalise all vectors to unit length (Ollama doesn't always do this).
            for v in &mut embeddings {
                normalise(v);
                // Verify dimension matches what we probed.
                if v.len() != *dim {
                    anyhow::bail!(
                        "HTTP embedding dimension mismatch: expected {}, got {}",
                        dim,
                        v.len()
                    );
                }
            }
            Ok(embeddings)
        }
    }
}

