/// Call an Ollama-compatible `/api/embed` endpoint.
///
/// Sends a batch of texts and returns one embedding vector per text.
fn http_embed_request(url: &str, model: &str, texts: &[&str]) -> Result<Vec<Vec<f32>>> {
    use std::io::{Read, Write};
    use std::net::TcpStream;

    // Parse URL to extract host:port and path.
    let url_str = if url.ends_with('/') { &url[..url.len() - 1] } else { url };
    let without_scheme = url_str
        .strip_prefix("http://")
        .ok_or_else(|| anyhow::anyhow!("embed URL must start with http://: {url}"))?;
    let (host_port, _) = without_scheme.split_once('/').unwrap_or((without_scheme, ""));
    let api_path = "/api/embed";

    // Tell Ollama to truncate inputs that exceed the model's context window.
    // Also cap on our side at ~1500 chars as a safety net — some model variants
    // ignore the truncate flag.
    const MAX_CHARS: usize = 1500;
    let truncated: Vec<String> = texts
        .iter()
        .map(|t| t.chars().take(MAX_CHARS).collect())
        .collect();
    let input: Vec<&str> = truncated.iter().map(|s| s.as_str()).collect();
    let body = serde_json::json!({
        "model": model,
        "input": input,
        "truncate": true,
    })
    .to_string();

    // Raw HTTP POST (no external HTTP crate dependency).
    let mut stream = TcpStream::connect(host_port)
        .with_context(|| format!("connecting to embed endpoint {host_port}"))?;
    stream
        .set_read_timeout(Some(std::time::Duration::from_secs(300)))
        .ok();

    let request = format!(
        "POST {api_path} HTTP/1.1\r\n\
         Host: {host_port}\r\n\
         Content-Type: application/json\r\n\
         Content-Length: {}\r\n\
         Connection: close\r\n\
         \r\n\
         {body}",
        body.len()
    );
    stream.write_all(request.as_bytes())?;

    // Read response.
    let mut response = String::new();
    stream.read_to_string(&mut response)?;

    // Split headers from body.
    let header_end = response
        .find("\r\n\r\n")
        .map(|i| (i, i + 4))
        .or_else(|| response.find("\n\n").map(|i| (i, i + 2)))
        .ok_or_else(|| anyhow::anyhow!("malformed HTTP response from embed endpoint"))?;
    let headers = &response[..header_end.0];
    let raw_body = &response[header_end.1..];

    // Handle chunked transfer encoding: strip chunk-size lines.
    let json_body = if headers.to_lowercase().contains("transfer-encoding: chunked") {
        decode_chunked(raw_body)
    } else {
        raw_body.to_string()
    };

    // Parse Ollama response: {"embeddings": [[f32, ...], ...]}
    let parsed: serde_json::Value = serde_json::from_str(&json_body)
        .with_context(|| format!("parsing embed response JSON (body length: {} bytes)", json_body.len()))?;

    let embeddings = parsed
        .get("embeddings")
        .and_then(|v| v.as_array())
        .ok_or_else(|| {
            // Include the response in the error for debugging.
            let preview = if json_body.len() > 200 { &json_body[..200] } else { &json_body };
            anyhow::anyhow!("embed response missing 'embeddings' array. Response: {preview}")
        })?;

    let mut result = Vec::with_capacity(embeddings.len());
    for emb in embeddings {
        let vec: Vec<f32> = emb
            .as_array()
            .ok_or_else(|| anyhow::anyhow!("embedding is not an array"))?
            .iter()
            .map(|v| v.as_f64().unwrap_or(0.0) as f32)
            .collect();
        result.push(vec);
    }

    Ok(result)
}

/// Decode an HTTP chunked transfer-encoded body.
///
/// Format: each chunk is `<hex-length>\r\n<data>\r\n`, terminated by `0\r\n\r\n`.
fn decode_chunked(raw: &str) -> String {
    let mut result = String::new();
    let mut remaining = raw;
    loop {
        // Skip leading whitespace/newlines.
        remaining = remaining.trim_start();
        if remaining.is_empty() {
            break;
        }
        // Read the hex chunk size.
        let size_end = remaining.find('\r').or_else(|| remaining.find('\n')).unwrap_or(remaining.len());
        let size_str = remaining[..size_end].trim();
        let size = usize::from_str_radix(size_str, 16).unwrap_or(0);
        if size == 0 {
            break;
        }
        // Skip past the size line.
        remaining = &remaining[size_end..];
        if remaining.starts_with("\r\n") {
            remaining = &remaining[2..];
        } else if remaining.starts_with('\n') {
            remaining = &remaining[1..];
        }
        // Take `size` bytes of data.
        let chunk_end = size.min(remaining.len());
        result.push_str(&remaining[..chunk_end]);
        remaining = &remaining[chunk_end..];
    }
    result
}

/// Build tract tensors and run the model, returning the raw TVec<TValue> outputs.
///
/// Rust note: `tract_onnx::tract_ndarray` re-exports ndarray under tract's own
/// version pin.  We use `Array2::from_shape_vec` to build a 2-D i64 array,
/// then call `.into()` which triggers the `From<Array<T,D>> for Tensor` impl
/// provided by tract-data.  `TValue::from(tensor)` wraps it in a `Var` variant
/// (heap-owned, non-shared tensor).
fn run_tract(
    model: &TractModel,
    has_token_type_ids: bool,
    batch: usize,
    seq_len: usize,
    ids_flat: &[i64],
    mask_flat: &[i64],
    type_flat: &[i64],
) -> Result<TVec<TValue>> {
    use tract_onnx::prelude::tract_ndarray::Array2;

    // Build [batch, seq_len] i64 arrays using tract's re-exported ndarray.
    let input_ids: Tensor = Array2::<i64>::from_shape_vec((batch, seq_len), ids_flat.to_vec())
        .context("building input_ids array")?
        .into();

    let attention_mask: Tensor =
        Array2::<i64>::from_shape_vec((batch, seq_len), mask_flat.to_vec())
            .context("building attention_mask array")?
            .into();

    if has_token_type_ids {
        let token_type_ids: Tensor =
            Array2::<i64>::from_shape_vec((batch, seq_len), type_flat.to_vec())
                .context("building token_type_ids array")?
                .into();

        model
            .run(tvec![
                input_ids.into(),
                attention_mask.into(),
                token_type_ids.into(),
            ])
            .context("tract inference (3-input)")
    } else {
        model
            .run(tvec![input_ids.into(), attention_mask.into(),])
            .context("tract inference (2-input)")
    }
}

/// Extract embeddings from tract outputs, update `dim`, and L2-normalise.
///
/// Handles two output formats:
/// - `sentence_embedding` [batch, dim]         — already pooled
/// - `last_hidden_state`  [batch, seq_len, dim] — mean-pooled with attention mask
///
/// `is_pre_pooled` is detected once during `load()` and cached.
fn extract_embeddings(
    outputs: &TVec<TValue>,
    dim: usize,
    is_pre_pooled: bool,
    mask_flat: &[i64],
    batch_size: usize,
    seq_len: usize,
) -> Result<Vec<Vec<f32>>> {
    // `outputs[0]` is a TValue; deref gives us &Tensor (via Deref impl).
    let out0: &Tensor = &outputs[0];

    if is_pre_pooled {
        // Shape: [batch, emb_dim]
        let shape = out0.shape();
        let emb_dim = shape.get(1).copied().unwrap_or(dim);
        // as_slice::<f32>() returns TractResult<&[f32]> (type-checked access to raw bytes)
        let data = out0
            .as_slice::<f32>()
            .context("extracting sentence_embedding f32 data")?;
        Ok(data
            .chunks(emb_dim)
            .map(|row| {
                let mut v = row.to_vec();
                normalise(&mut v);
                v
            })
            .collect())
    } else {
        // Shape: [batch, seq_len, emb_dim]
        let shape = out0.shape();
        let b = shape.first().copied().unwrap_or(batch_size);
        let s = shape.get(1).copied().unwrap_or(seq_len);
        let emb_dim = shape.get(2).copied().unwrap_or(dim);

        let data = out0
            .as_slice::<f32>()
            .context("extracting last_hidden_state f32 data")?;

        let mut result = Vec::with_capacity(b);
        for bi in 0..b {
            let mut pooled = vec![0f32; emb_dim];
            let mut total_mask: f32 = 0.0;
            for si in 0..s {
                let mask_val = mask_flat[bi * seq_len + si] as f32;
                total_mask += mask_val;
                for di in 0..emb_dim {
                    pooled[di] += data[bi * s * emb_dim + si * emb_dim + di] * mask_val;
                }
            }
            if total_mask > 0.0 {
                for x in &mut pooled {
                    *x /= total_mask;
                }
            }
            normalise(&mut pooled);
            result.push(pooled);
        }
        Ok(result)
    }
}

// ---------------------------------------------------------------------------
// Stub embedding: deterministic unit vector from text
// ---------------------------------------------------------------------------

/// Generate a deterministic unit vector of `dim` f32s from `text`.
///
/// Algorithm:
/// 1. Compute a 64-bit FNV-1a hash of the text bytes.  This is the LCG seed.
/// 2. Run an LCG (Knuth's multiplicative constants) to fill `dim` values in [-1.0, 1.0].
/// 3. L2-normalise the result.
fn stub_embed(text: &str, dim: usize) -> Vec<f32> {
    // FNV-1a seed
    let mut hash: u64 = 14_695_981_039_346_656_037;
    for byte in text.as_bytes() {
        hash ^= *byte as u64;
        hash = hash.wrapping_mul(1_099_511_628_211);
    }

    const A: u64 = 6_364_136_223_846_793_005;
    const C: u64 = 1_442_695_040_888_963_407;

    let mut state = hash;
    let mut v: Vec<f32> = Vec::with_capacity(dim);
    for _ in 0..dim {
        state = state.wrapping_mul(A).wrapping_add(C);
        let bits = (state >> 32) as u32;
        let f = (bits as f32 / 2_147_483_648.0) - 1.0;
        v.push(f);
    }

    normalise(&mut v);
    v
}

/// In-place L2 normalisation.  No-op if the norm is near zero.
fn normalise(v: &mut [f32]) {
    let norm: f32 = v.iter().map(|x| x * x).sum::<f32>().sqrt();
    if norm > 1e-9 {
        for x in v.iter_mut() {
            *x /= norm;
        }
    }
}

